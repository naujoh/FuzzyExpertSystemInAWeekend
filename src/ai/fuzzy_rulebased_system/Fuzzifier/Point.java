package ai.fuzzy_rulebased_system.Fuzzifier;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Point() { }

    public double getX() { return x; }

    public void setX(float x) { this.x = x; }

    public double getY() { return y; }

    public void setY(float y) { this.y = y; }
}
